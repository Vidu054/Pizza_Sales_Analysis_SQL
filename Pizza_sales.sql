**** Pizza Sales Analysis ****
--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Total Revenue Generated
  
SELECT SUM(total_price) as Total_Revenue
FROM pizza_sales

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Total Pizza Sold

SELECT SUM(quantity) as Total_Pizza_Sold
FROM pizza_sales

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
-- Average Order Value

SELECT SUM(total_price)/count(distinct(order_id)) as Avg_order_value
FROM pizza_sales
